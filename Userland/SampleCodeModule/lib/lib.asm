%macro pushStateNoRAX 0
    push rbx
    push rcx
    push rdx
    push rbp
    push rdi
    push rsi
    push r8
    push r9
    push r10
    push r11
    push r12
    push r13
    push r14
    push r15
%endmacro

%macro popStateNoRAX 0
    pop r15
    pop r14
    pop r13
    pop r12
    pop r11
    pop r10
    pop r9
    pop r8
    pop rsi
    pop rdi
    pop rbp
    pop rdx
    pop rcx
    pop rbx
%endmacro

%macro pushState 0
    push rax
    pushStateNoRAX
%endmacro

%macro popState 0
    popStateNoRAX
    pop rax
%endmacro

%macro do_syscall_direct 1
    pushStateNoRAX
		mov rax, %1
    int 80h
    popStateNoRAX
    ret
%endmacro

%macro do_syscall 4
    pushStateNoRAX
    mov rax, %1
    mov rdx, %2
    mov rsi, %3
    mov rdi, %4
    int 80h
    popStateNoRAX
    ret
%endmacro

%macro EXPORT_FUNC 1
    GLOBAL _%1
    _%1:
%endmacro

